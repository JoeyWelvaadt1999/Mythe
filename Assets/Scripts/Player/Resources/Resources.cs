using UnityEngine;
using System.Collections;

public class Resources {
	private int _wood;
	private int _stones;
	private int _meat;
	private int _fur;

	public int Wood {
		get { 
			return _wood;
		}

		set {
			_wood = value;
		}
	}

	public int Stones {
		get { 
			return _stones;
			}

		set {
			_stones = value;
		}
	}

	public int Meat {
		get { 
			return _meat;
			}

		set {
			_meat = value;
		}
	}

	public int Fur {
		get { 
			return _fur;
			}

		set {
			_fur = value;
		}
	}


}
